/*
call stack is a region of memory in RAM, where all the 
function calls of a particular program takes place.

important points:
1. everytime a function is called it gets created in the stack memory.
2. all the local variables belonging to that function are the part of the stack frame.
3. stack frame is also called as activation record which is created when a function is called.
4. once the function call ends the stack frame for that function is cleared off.
5. the function that is currently executing is always on top of the stack.
*/
