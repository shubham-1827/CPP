#include <iostream>
using namespace std;

int main()
{
  char ch = 'a';
  cout << ch << endl;

  int val = ch;

  // each character has an ascii value associated with it, like for a its 97
  cout << val << endl;

  // remember these six ascii values -- a = 97, z = 122, A = 65, Z = 90, 0 = 48, 9 = 57
  return 0;
}